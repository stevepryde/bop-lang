/// Resolve and preflight a module export used with method-call syntax
/// (`module.export(...)`). The returned callable is the exact value that must
/// later be invoked, so neither the receiver nor a live export is resolved a
/// second time after argument side effects.
fn __bop_preflight_module_call(
    ctx: &Ctx<'_>,
    receiver: &::bop::value::Value,
    method: &str,
    actual_modes: &[::bop::parser::ParamMode],
    line: u32,
) -> Result<::std::option::Option<::bop::value::Value>, ::bop::error::BopError> {
    let ::bop::value::Value::Module(module) = receiver else {
        return Ok(None);
    };
    // Universal value methods have precedence over module exports.
    if matches!(method, "type" | "to_str" | "inspect") {
        return Ok(None);
    }
    let callable = __bop_module_member(ctx, module, method).ok_or_else(|| {
        ::bop::error::BopError::runtime(
            format!("`{}` isn't exported from `{}`", method, module.path),
            line,
        )
    })?;
    __bop_preflight_value_call(ctx, &callable, actual_modes, line)?;
    Ok(Some(callable))
}

